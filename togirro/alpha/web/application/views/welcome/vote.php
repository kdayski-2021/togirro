<script>
	$(document).ready(() => {
		var cards = $('.card-participant-vote')
		for(i=0;i<=cards.length;i++) {
			let category = $(cards[i]).attr('category')
			$(`.card-participant-vote-wrapper-${category}`).append($(cards[i]))
		}

		$('.slide-schedule').click((e) => {
			$(e.target).next().slideToggle('slow')
			$(e.target).parent().next().toggleClass('up')
			if ($(e.target).parent().next().attr('class').includes('up')) {
				$(e.target).parent().next().css('transform', 'rotate(-135deg)')
				$(e.target).parent().next().css('-webkit-transform', 'rotate(-135deg)')
			}  else {
				$(e.target).parent().next().css('transform', 'rotate(45deg)')
				$(e.target).parent().next().css('-webkit-transform', 'rotate(45deg)')
			}
		})
		$('.arrow').click((e) => {
			$($(e.target).prev().children()[1]).slideToggle('slow')
			$(e.target).toggleClass('up')
			if ($(e.target).attr('class').includes('up')) {
				$(e.target).css('transform', 'rotate(-135deg)')
				$(e.target).css('-webkit-transform', 'rotate(-135deg)')
			}  else {
				$(e.target).css('transform', 'rotate(45deg)')
				$(e.target).css('-webkit-transform', 'rotate(45deg)')
			}
		})
	})
</script>